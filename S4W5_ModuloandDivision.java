public class Main
{
    public static void main(String[] args) {
        int counter = 34;
        int m = 6;
        int resultDiv = counter / m;
        int resultMod = counter % m;
        System.out.println("Result of Division: " + resultDiv);
        System.out.println("Result of Modulo: " + resultMod);
    }
}
